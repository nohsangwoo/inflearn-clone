export function getTranslation(locale: string = 'ko'): Translation {
  const validLocale = (supportedLocales.includes(locale as Locale) ? locale : 'ko') as Locale;
  const translation = translations[validLocale];
  // Fill missing translations with defaults to ensure type safety
  return fillMissingTranslations(translation) as Translation;
}

export function useLocale(pathname: string): Locale {
  const segments = pathname.split('/').filter(Boolean);
  const firstSegment = segments[0];

  if (firstSegment && supportedLocales.includes(firstSegment as Locale)) {
    return firstSegment as Locale;
  }

  return 'ko';
}